package com.cpts487.shapes;

public class Style {
	
	private int color;
	private int fill;
	private int shade;
	
	public Style(int color, int fill, int shade) {
		this.color = color;
		this.fill = fill;
		this.shade = shade;
	}
	
	public int getColor() {
		return color;
	}
	
	public int getFill() {
		return fill;
	}
	
	public int getShade() {
		 return shade;
	}
	
}
